def encode():
    mssg = input("Enter your message: ")
    encoded = []
    shift = int(input("Enter your shift: "))
    for i in mssg:
        encoded.append(alphabets[alphabets.index(i)+ shift])

    for i in encoded:
        print(i, end = "")

def decode():
    mssg = input("Enter your message: ")
    decoded = []
    shift = int(input("Enter your shift: "))
    for i in mssg:
        decoded.append(alphabets[alphabets.index(i)- shift])

    for i in decoded:
        print(i, end= "")

while True:
    action = input("\n\nDo you want to encode or decode a message? (type exit to stop): ")

    if action.lower() == "encode":
        encode()

    elif action.lower() == "decode":
        decode()

    elif action.lower() == "exit":
        break

    else:
        print("Error")
        break

